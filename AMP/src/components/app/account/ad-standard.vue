						<h2>二、国美互联网广告运营规范</h2>
						<p>根据《中华人民共和国广告法》、《互联网广告管理暂行办法》等相关法规，互联网信息服务单位等大众传播媒介应当履行法定的广告审查义务，在广告发布前查验相关广告证明文件、核实广告内容，确保广告真实、合法。按照国家广告监管部门的要求及本规范，审查范围包括国美互联网广告下国美Plus及国美在线平台上所有广告资源及广告素材，广告素材均指广告素材内容和广告点击链接相关页面的内容；</p>
